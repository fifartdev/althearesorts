// ─── helpers ──────────────────────────────────────────────────────────────────

function para(text: string) {
  return {
    children: [{ detail: 0, format: 0, mode: 'normal', style: '', text, type: 'text', version: 1 }],
    direction: 'ltr', format: '', indent: 0, type: 'paragraph', version: 1,
  }
}
function richText(...paragraphs: string[]) {
  return { root: { children: paragraphs.map(para), direction: 'ltr', format: '', indent: 0, type: 'root', version: 1 } }
}

const report: { updated: string[]; notFound: string[]; errors: string[] } = {
  updated: [],
  notFound: [],
  errors: [],
}

async function updateDoc(
  payload: Awaited<ReturnType<typeof getPayload>>,
  collection: string,
  slug: string,
  data: Record<string, unknown>,
) {
  try {
    const res = await (payload.find as Function)({
      collection,
      where: { slug: { equals: slug } },
      limit: 1,
    })
    if (!res.docs.length) { report.notFound.push(`${collection}/${slug}`); return }
    await (payload.update as Function)({ collection, id: res.docs[0].id, locale: 'el', data })
    report.updated.push(`${collection}/${slug}`)
  } catch (err: any) {
    report.errors.push(`${collection}/${slug}: ${err?.message ?? String(err)}`)
  }
}

